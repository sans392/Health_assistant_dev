"""Статический ответ на мета-вопросы о возможностях ассистента.

Intent `capability_question` («что ты умеешь?», «какие у тебя функции?») маршрутизируется
напрямую в fast_direct_answer с этим готовым ответом — без LLM-вызова, чтобы минимизировать
задержку и гарантировать одинаковый ответ.
"""


_CAPABILITY_ANSWER = """Я — локальный фитнес-ассистент. Вот что я умею:

- **Данные о твоих тренировках и здоровье**: показываю историю активностей,
  шаги, сон, пульс, HRV, calories, recovery score, strain.
- **Анализ и оценка**: «это нормально?», динамика/прогресс, сравнение периодов,
  проверка перетренированности и уровня восстановления.
- **Планы тренировок**: недельный план, отчёт по прогрессу, план восстановления.
- **Ответы на вопросы о нормах**: «сколько белка нужно», «норма шагов»,
  «что такое HRV» — из моей базы знаний.
- **Жалобы на здоровье**: подскажу, что делать при болях/дискомфорте,
  и порекомендую обратиться к врачу при тревожных симптомах.

Примеры запросов:
- «Покажи мои пробежки за неделю»
- «Это нормально — 5000 шагов в день?»
- «Составь план тренировок на неделю»
- «Сколько белка нужно спортсмену?»
- «Что такое HRV?»
"""


def build_capability_answer() -> str:
    """Вернуть статический ответ на «Что ты умеешь?»."""
    return _CAPABILITY_ANSWER
